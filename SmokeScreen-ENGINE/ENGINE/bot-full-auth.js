// Key redemption command
async function redeemKey(userId, key) {
    try {
        // Check if key exists and is valid
        const keyData = generatedKeys.get(key);
        if (!keyData) {
            return { success: false, message: '❌ Invalid key. This key was not generated by ENGINE.exe.' };
        }

        // Check if key is already redeemed
        if (keyData.redeemed) {
            return { success: false, message: '❌ This key has already been redeemed.' };
        }

        // Check if key is expired
        if (keyData.expiresAt && Date.now() > keyData.expiresAt) {
            return { success: false, message: '❌ This key has expired.' };
        }

        // Check if user has permission (based on Discord roles)
        const guild = client.guilds.cache.get(GUILD_ID);
        if (!guild) {
            return { success: false, message: '❌ Guild not found.' };
        }

        const member = await guild.members.fetch(userId).catch(() => null);
        if (!member) {
            return { success: false, message: '❌ You must be in the Discord server to redeem keys.' };
        }

        // Check if user has required role
        const hasRequiredRole = member.roles.cache.some(role => 
            role.id === '1455256056312631376' || // OWNER
            role.id === '1455256063153803304' || // COMMUNITY MANAGER
            role.id === '1477448046873935872'    // BASIC ACCESS
        );

        if (!hasRequiredRole) {
            return { success: false, message: '❌ You need the required Discord role to redeem keys.' };
        }

        // Validate recoil key format and grant access
        let gameAccess = null;
        let keyType = 'MAIN LICENSE';
        
        if (key.startsWith('R6S-')) {
            gameAccess = 'Rainbow Six Siege';
            keyType = 'RECOIL R6S';
        } else if (key.startsWith('CODW-')) {
            gameAccess = 'Call of Duty Warzone';
            keyType = 'RECOIL CODW';
        } else if (key.startsWith('AR-')) {
            gameAccess = 'Arc Raiders';
            keyType = 'RECOIL AR';
        } else if (key.startsWith('FN-')) {
            gameAccess = 'Fortnite';
            keyType = 'RECOIL FN';
        }

        // Redeem key
        keyData.redeemed = true;
        keyData.redeemedBy = userId;
        keyData.redeemedAt = Date.now();
        keyData.redeemedByUsername = member.user.tag;
        
        saveKeys();

        let successMessage = `✅ Key redeemed successfully!\n\n**Key Details:**\n• Type: ${keyType}\n• Game: ${gameAccess || keyData.game || 'Unknown'}\n• Duration: ${keyData.duration}\n• Redeemed by: ${member.user.tag}\n• Redeemed at: ${new Date().toLocaleString()}`;

        // Add specific game access information
        if (gameAccess) {
            successMessage += `\n\n🎮 **Game Access Granted:**\nYou now have access to ${gameAccess} Recoil V2 features!\n\n💡 **How to use:**\n1. Open SmokeScreen-Engine.exe\n2. Navigate to the ${gameAccess} tab\n3. Your Recoil V2 features are now active`;
        }

        return { 
            success: true, 
            message: successMessage
        };
    } catch (error) {
        console.error('[BOT] Error redeeming key:', error);
        return { success: false, message: '❌ An error occurred while redeeming the key.' };
    }
}

// Auto-reconnect function
function reconnectBot() {
    if (reconnectAttempts >= MAX_RECONNECT_ATTEMPTS) {
        console.log(`[BOT] Max reconnection attempts (${MAX_RECONNECT_ATTEMPTS}) reached. Stopping...`);
        process.exit(1);
    }
    
    reconnectAttempts++;
    console.log(`[BOT] Attempting to reconnect... (${reconnectAttempts}/${MAX_RECONNECT_ATTEMPTS})`);
    
    setTimeout(() => {
        client.login(BOT_TOKEN).catch(error => {
            console.error(`[BOT] Reconnection failed:`, error);
            if (error.code === 50001) { // Missing permissions
                console.log('[BOT] Critical: Bot lacks permissions. Please check bot permissions.');
                process.exit(1);
            } else {
                reconnectBot();
            }
        });
    }, Math.min(1000 * Math.pow(2, reconnectAttempts), 30000)); // Exponential backoff, max 30s
}

